package com.fusheng.personal.finance.model;

public class TransEnum {
	private int id;
	private int enumKey;
	private String enumValue;
	private String enumCatagory;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getEnumKey() {
		return enumKey;
	}
	public void setEnumKey(int enumKey) {
		this.enumKey = enumKey;
	}
	public String getEnumValue() {
		return enumValue;
	}
	public void setEnumValue(String enumValue) {
		this.enumValue = enumValue;
	}
	public String getEnumCatagory() {
		return enumCatagory;
	}
	public void setEnumCatagory(String enumCatagory) {
		this.enumCatagory = enumCatagory;
	}
}
